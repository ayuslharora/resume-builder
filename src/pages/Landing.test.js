  assert.match(cssSource, /html\[data-route="\/"\]\s+body[\s\S]*background-image:\s*none/);
  assert.doesNotMatch(landingSource, /\b(?:py-20|lg:py-24|pb-20|mb-12)\b/);
  assert.doesNotMatch(seoSource, /\b(?:py-20|lg:py-24|pb-20|mb-12)\b/);
