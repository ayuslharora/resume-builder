  assert.match(landingSource, /gap:\s*56px/);
  assert.match(landingSource, /padding:\s*"20px 24px"/);
  assert.match(landingSource, /Trusted by candidates targeting roles at/);
  assert.match(landingSource, /landing-company-ticker/);
  assert.match(landingSource, /landing-company-track/);
  assert.match(landingSource, /landing-company-segment/);
  assert.match(landingSource, /paddingTop:\s*96,\s*paddingBottom:\s*64/);
  assert.match(landingSource, /marginBottom:\s*56/);
  assert.match(landingSource, /paddingTop:\s*32,\s*paddingBottom:\s*64/);
  assert.match(landingSource, /paddingTop:\s*64,\s*paddingBottom:\s*96/);
  assert.match(landingSource, /padding:\s*"56px 48px"/);
  assert.match(publicFooterSource, /padding:\s*"28px 24px"/);
  assert.match(staticShell, /style="padding-top:88px;padding-bottom:96px"/);
  assert.match(staticShell, /grid-template-columns:1\.05fr \.95fr;gap:56px/);
  assert.match(staticShell, /style="display:flex;align-items:center;gap:36px;padding:20px 24px;overflow:hidden"/);
  assert.match(staticShell, /Trusted by candidates targeting roles at/);
  assert.match(staticShell, /landing-company-ticker/);
  assert.match(staticShell, /landing-company-track/);
  assert.match(staticShell, /landing-company-segment/);
  assert.match(staticShell, /style="padding-top:96px;padding-bottom:64px"/);
  assert.match(staticShell, /style="max-width:720px;margin-bottom:56px"/);
  assert.match(staticShell, /style="padding-top:32px;padding-bottom:64px"/);
  assert.match(staticShell, /style="padding-top:64px;padding-bottom:96px"/);
  assert.match(staticShell, /style="border-radius:18px;padding:56px 48px/);
  assert.match(staticShell, /style="display:flex;align-items:center;gap:16px;padding:28px 24px;flex-wrap:wrap"/);
  assert.match(landingSource, /Resu<span className="serif italic font-normal">Me<\/span>/);
  assert.match(landingSource, /The resume tool|HOME_SEO_TITLE/);
  assert.match(landingSource, /ATS Grade/);
  assert.match(landingSource, /scorebar/);
  assert.match(landingSource, /aspectRatio:\s*"1\.05"/);
  assert.match(seoSource, /aspect-ratio:1\.05/);
  assert.match(landingSource, /Unlimited AI Resume Builder|HOME_SEO_TITLE/);
